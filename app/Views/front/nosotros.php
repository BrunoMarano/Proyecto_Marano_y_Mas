    <h5 class="nosotros-principal-titulo">Acerca de nosotros</h5>
    <p class="nosotros-principal-parrafo">
        Avícola Santa Ana S.A. es una empresa líder en la producción de huevos en la provincia de Corrientes, Argentina.
        Fundada en 1985, somos la primera y única compañía avícola de la región, 
        con una trayectoria de más de 35 años dedicada a ofrecer productos frescos, nutritivos y de calidad
        En el año 2006, con una renovada política comercial, la empresa abre las puertas 
        de un centro de ventas y distribución en la ciudad de Corrientes, situado en Av. Independencia 4437.
        Este año la empresa logra nuevas alianzas que grandes firmas de Entre Ríos y Santa Fe, lo que genera
        el inicio de un fuerte proceso de inversiones de capital industrial y tecnológico, incluyendo la habilitación de SENASA.
        En 2014 la empresa emprende una fuerte ampliación y reconversión de su producción de huevos.
        La incorporación genero un incremento del 190% de su producción lo que permitió 
        tener como nuevo objetivo la exportación de sus productos.
    </p>
